CREATE OR REPLACE PROCEDURE "P_TEST_S_ST_BRANCH"

 AS
TRANS_NAME VARCHAR2(50) :='P_TEST_S_ST_BRANCH' ; --存储过程名

V_TABLE_NAME VARCHAR2(50) :='ST_BRANCH';  -- 测试目标表名
V_COL VARCHAR2(50);     --字段英文名
V_COL_NAME VARCHAR2(50);     --字段中文名
V_COL_DES VARCHAR2(50);     --字段描述
V_TEST_DES VARCHAR2(50);     --测试描述
V_TEST_STAGE VARCHAR2(50) :='S_ST';  -- 测试阶段
V_TEST_TIMESTAMP TIMESTAMP;  -- 加载测试时间

--存储过程日志变量
V_START_TIMESTAMP TIMESTAMP;  -- 加载开始时间
V_END_TIMESTAMP    TIMESTAMP;  -- 加载结束时间
IO_ROW INTEGER;
IO_STATUS INTEGER;
VO_SQLERR VARCHAR2(5000);          -- 返回的描述

BEGIN
SELECT SYSDATE INTO V_START_TIMESTAMP FROM dual;  -- 加载程序运行开始时间
V_TEST_TIMESTAMP := V_START_TIMESTAMP;

--1.字符型字段
--如果表中的字符型数据没有特殊处理，则仅测试表记录数即可;如果有特殊处理，则再做个别字段逻辑测试。
--表记录数
V_COL := 'DMMKT_BRNH_ID';
V_COL_NAME := '分支行总数';
V_COL_DES := '所有分支行总记录数';
V_TEST_DES := '计数';

INSERT INTO MMAPTEST.MMAP_TEST
  (
    TEST_DATE          --  测试时间
    ,TEST_STAGE        --  测试阶段
    ,TABLE_NAME      --  目标表名
    ,TX_DATE      --  数据日期
    ,COL      -- 字段英文名
    ,COL_NAME    --  字段中文名
    ,COL_DES    --  字段描述
    ,TEST_DES    --  测试描述
    ,VALUE_SOURCE    -- 字段值（源）
    ,VALUE_TARGET    --  字段值（目标）
    ,COMPARE_RESULT    --  对比结果
    ,REMARK    --  备注

  )
select
V_TEST_TIMESTAMP
,V_TEST_STAGE
,V_TABLE_NAME
,NULL
,V_COL
,V_COL_NAME
,V_COL_DES
,V_TEST_DES
,T1.S_NUM
,T2.ST_NUM
,NVL(T1.S_NUM,0)-NVL(T2.ST_NUM,0)
,NULL
FROM
(
SELECT 'AA' AS TX_DATE,COUNT(BRNH_ID)  AS S_NUM    --修改
FROM
 (
SELECT * FROM MMAPST.DMMKT_RLBRNH 
WHERE BRNH_ID  NOT IN('80002' ,'0','90000','88000')
AND OBRNH_ID <> '80002'
UNION
SELECT * FROM MMAPST.DMMKT_RLBRNH 
WHERE OBRNH_ID='80002' 
AND BRNH_ID IS NOT NULL
AND BRNH_NM IS NOT NULL
AND BRNH_ID NOT IN('80002' ,'0')
) a
) T1
FULL JOIN
(
SELECT 'AA' AS TX_DATE,COUNT(1) AS ST_NUM  
FROM   MMAPST.ST_BRANCH A
)T2
ON T1.TX_DATE=T2.TX_DATE;

--2.字符型字段
--如果表中的字符型数据没有特殊处理，则仅测试表记录数即可;如果有特殊处理，则再做个别字段逻辑测试。
--表记录数
V_COL := 'DMMKT_REGION_ID';
V_COL_NAME := '分行总数';
V_COL_DES := '每个分行下的支行';
V_TEST_DES := '计数';

INSERT INTO MMAPTEST.MMAP_TEST
  (
    TEST_DATE          --  测试时间
    ,TEST_STAGE        --  测试阶段
    ,TABLE_NAME      --  目标表名
    ,TX_DATE      --  数据日期
    ,COL      -- 字段英文名
    ,COL_NAME    --  字段中文名
    ,COL_DES    --  字段描述
    ,TEST_DES    --  测试描述
    ,VALUE_SOURCE    -- 字段值（源）
    ,VALUE_TARGET    --  字段值（目标）
    ,COMPARE_RESULT    --  对比结果
    ,REMARK    --  备注

  )
select
V_TEST_TIMESTAMP
,V_TEST_STAGE
,V_TABLE_NAME
,NULL
,V_COL
,V_COL_NAME
,V_COL_DES
,V_TEST_DES
,T1.S_REGION_COUNT
,T2.ST_REGION_COUNT
,NVL(T2.ST_REGION_COUNT,0)-NVL(T1.S_REGION_COUNT,0)
,NULL
FROM
(
SELECT 'AA' AS TX_DATE,COUNT(1) AS S_REGION_COUNT,REGION_ID FROM 
(
SELECT SUBSTR(BRNH_ID,1,2）AS REGION_ID FROM MMAPST.DMMKT_RLBRNH
WHERE BRNH_ID  NOT IN('80002' ,'0','90000','88000')
AND BRNH_ID IS NOT NULL
AND BRNH_NM IS NOT NULL
) A 
GROUP BY REGION_ID
ORDER BY REGION_ID
)T1
FULL JOIN
(
SELECT 'AA' AS TX_DATE,COUNT(1) AS ST_REGION_COUNT, REGION_ID FROM MMAPST.ST_BRANCH
GROUP BY REGION_ID
ORDER BY REGION_ID
)T2
ON T1.TX_DATE=T2.TX_DATE AND T1.REGION_ID=T2.REGION_ID;

--3.字符型字段
--如果表中的字符型数据没有特殊处理，则仅测试表记录数即可;如果有特殊处理，则再做个别字段逻辑测试。
--表记录数
V_COL := 'DMMKT_FBRNH_COUNT';
V_COL_NAME := '一级分行或二级支行个数';
V_COL_DES := '一级分行或二级支行个数';
V_TEST_DES := '计数';

INSERT INTO MMAPTEST.MMAP_TEST
  (
    TEST_DATE          --  测试时间
    ,TEST_STAGE        --  测试阶段
    ,TABLE_NAME      --  目标表名
    ,TX_DATE      --  数据日期
    ,COL      -- 字段英文名
    ,COL_NAME    --  字段中文名
    ,COL_DES    --  字段描述
    ,TEST_DES    --  测试描述
    ,VALUE_SOURCE    -- 字段值（源）
    ,VALUE_TARGET    --  字段值（目标）
    ,COMPARE_RESULT    --  对比结果
    ,REMARK    --  备注

  )
select
V_TEST_TIMESTAMP
,V_TEST_STAGE
,V_TABLE_NAME
,NULL
,V_COL
,V_COL_NAME
,V_COL_DES
,V_TEST_DES
,T1.S_FBRNH_COUNT
,T2.ST_FBRNH_COUNT
,NVL(T1.S_FBRNH_COUNT,0)-NVL(T2.ST_FBRNH_COUNT,0)
,NULL
FROM
(
SELECT 'AA' AS TX_DATE,COUNT(*) AS S_FBRNH_COUNT
FROM MMAPST.DMMKT_RLBRNH a
WHERE OBRNH_ID='80002' 
AND BRNH_ID IS NOT NULL
AND BRNH_NM IS NOT NULL
AND BRNH_ID NOT IN('80002' ,'0')
)T1
FULL JOIN 
(
SELECT 'AA' AS TX_DATE,COUNT(distinct FBRNH_ID) AS ST_FBRNH_COUNT FROM MMAPST.ST_BRANCH
)T2
ON T1.TX_DATE=T2.TX_DATE;

--4.字符型字段
--如果表中的字符型数据没有特殊处理，则仅测试表记录数即可;如果有特殊处理，则再做个别字段逻辑测试。
--表记录数
V_COL := 'DMMKT_BRNH_COUNT';
V_COL_NAME := '支行个数';
V_COL_DES := '支行个数';
V_TEST_DES := '计数';

INSERT INTO MMAPTEST.MMAP_TEST
  (
    TEST_DATE          --  测试时间
    ,TEST_STAGE        --  测试阶段
    ,TABLE_NAME      --  目标表名
    ,TX_DATE      --  数据日期
    ,COL      -- 字段英文名
    ,COL_NAME    --  字段中文名
    ,COL_DES    --  字段描述
    ,TEST_DES    --  测试描述
    ,VALUE_SOURCE    -- 字段值（源）
    ,VALUE_TARGET    --  字段值（目标）
    ,COMPARE_RESULT    --  对比结果
    ,REMARK    --  备注

  )
select
V_TEST_TIMESTAMP
,V_TEST_STAGE
,V_TABLE_NAME
,NULL
,V_COL
,V_COL_NAME
,V_COL_DES
,V_TEST_DES
,T1.S_BRNH_COUNT
,T2.ST_BRNH_COUNT
,NVL(T1.S_BRNH_COUNT,0)-NVL(T2.ST_BRNH_COUNT,0)
,NULL
FROM
(
SELECT 'AA' AS TX_DATE,COUNT(*) AS S_BRNH_COUNT
FROM MMAPST.DMMKT_RLBRNH a
LEFT JOIN MMAPST.DMMKT_RLBRNH b
ON a.OBRNH_ID=B.BRNH_ID
WHERE a.OBRNH_ID IS NOT NULL
AND a.BRNH_ID IS NOT NULL
AND a.BRNH_NM IS NOT NULL
AND a.BRNH_ID  NOT IN('80002' ,'0','90000','88000')
AND a.OBRNH_ID <> '80002'
)T1
FULL JOIN 
(
SELECT 'AA' AS TX_DATE,COUNT(BRNH_ID) AS ST_BRNH_COUNT FROM MMAPST.ST_BRANCH WHERE BRNH_ID
NOT IN (
SELECT DISTINCT FBRNH_ID FROM MMAPST.ST_BRANCH
)
)T2 
ON T1.TX_DATE=T2.TX_DATE
;

IO_ROW := SQL%ROWCOUNT ; --记录存储过程记录数
COMMIT;
SELECT SYSDATE INTO  V_END_TIMESTAMP  FROM dual;  -- 加载程序运行结束时间
IO_STATUS := 0 ;
VO_SQLERR := 'SUSSCESS';

P_TEST_WRITE_LOGS(TRANS_NAME,IO_STATUS,IO_ROW,V_START_TIMESTAMP,V_END_TIMESTAMP,VO_SQLERR);

EXCEPTION
    WHEN OTHERS THEN
  ROLLBACK ;
  IO_STATUS := 9 ;
  VO_SQLERR := SQLCODE ||  SQLERRM  ;
  SELECT SYSDATE INTO  V_END_TIMESTAMP  FROM dual;
  P_TEST_WRITE_LOGS(TRANS_NAME,IO_STATUS,IO_ROW,V_START_TIMESTAMP,V_END_TIMESTAMP,VO_SQLERR);


END  P_TEST_S_ST_BRANCH;
/
