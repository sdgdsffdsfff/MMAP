TRUNCATE TABLE ST_CUST_RELATION;
INSERT INTO MMAPST.ST_CUST_RELATION 
(
	 ETL_DATE
	,TX_DATE
	,PERIOD_ID
	,CIF_ORG
	,RELCIF_ORG
	,RELTYP_CODE
) 
SELECT
	 TO_NUMBER(TO_CHAR(SYSDATE,'YYYYMMDD')) AS ETL_DATE  
	,TX_DT
	,TO_NUMBER(TO_CHAR(TX_DT,'YYYYMMDD')) AS PERIOD_ID
	,TRIM(CIF_ORG)
	,TRIM(NVL(RelCIF_ORG,'-'))
	,TRIM(NVL(RelTyp_ID,'-'))
FROM MMAPST.DMMKT_CSCIFREL