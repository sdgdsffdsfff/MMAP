DROP TABLE  MMAPST.ST_CUST_CARD;
CREATE TABLE MMAPST.ST_CUST_CARD
(
 ETL_DATE NUMBER(8)
,TX_DATE DATE
,PERIOD_ID NUMBER(8)
,CARD_ID VARCHAR2(50)
,ACCOUNT_ID VARCHAR2(50)
,OPEN_BRANCH VARCHAR2(50)
,ISSU_DT DATE
,CARDSTS_ID VARCHAR2(50)
,CREATECARD_DT DATE
,FEE_YAMT NUMBER(20,8)
,CUSTOMER_ID VARCHAR2(50)
,CRDTYP_ID VARCHAR2(50)
,EFFE_DT DATE
,CLOS_DT DATE
,EXPR_DT DATE
,B_TXLM NUMBER(20,8)
,AUTH_ID VARCHAR2(50)
,BCARDNO VARCHAR2(50)
,LM_BAIl NUMBER(20,8)
,CARD_FG VARCHAR2(50)
,ICCARD_ID VARCHAR2(50)
,SVC_NAME VARCHAR2(50)
);

