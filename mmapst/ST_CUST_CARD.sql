SELECT 
TO_NUMBER(to_char(SYSDATE,'YYYYMMDD')) AS ETL_DATE
,a.TX_DT AS TX_DATE
,TO_NUMBER(to_char(SYSDATE,'YYYYMMDD')) AS PERIOD_ID
,TRIM(CARD_ORG)
,TRIM(ACC_ORG)
,TRIM(BRNH_ID)
,TRIM(ISSU_DT)
,TRIM(CARDSTS_ID)
,TRIM(CREATECARD_DT)
,TRIM(FEE_YAMT)
,TRIM(a.CIF_ORG)
,NVL(TRIM(CRDTYP_ID),'-')
,TRIM(EFFE_DT)
,TRIM(CLOS_DT)
,TRIM(EXPR_DT)
,TRIM(B_TXLM)
,TRIM(AUTH_ID)
,TRIM(BCARDNO)
,TRIM(LM_BAIl)
,TRIM(CARD_FG)
,TRIM(ICCARD_ID)
,TRIM(SVC_NAME)
FROM DMMKT_ACDRCARD a
INNER JOIN MMAPST.DMMKT_CSMAST b
ON a.CIF_ORG=b.CIF_ORG
WHERE TRIM(b.CIFTYP_ID)='I'

